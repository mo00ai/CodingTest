package backjoon;

import java.util.Scanner;

public class P10869 {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int firstNum = sc.nextInt();
		int secondNum = sc.nextInt();

		System.out.println(firstNum + secondNum);
		System.out.println(firstNum - secondNum);
		System.out.println(firstNum * secondNum);
		System.out.println(firstNum / secondNum);
		System.out.println(firstNum % secondNum);
	}
}
